// Ejemplo: Verificar si una persona es mayor de edad
// Paso1: Declarmosla variable de edad de la persona+
let edad = 18;

//Paso 2: Usamo el operador ternario para determinar si es mayor de edad
let mensaje = edad >=18 ? "Mayor de edad" : "Menor de edad";
// La condición es : Edad es  mayor o igual a 18?
//Si es verdadera, el resultado sera "Mayor de edad"
// Si es falsa, el resultado sera "Meno de edad"

// Paso 3: mostramos por consola

console.log(mensaje);

// Explicación:
// El operador ternario funciona como un if pero es una sola linea
// Estructura: condicion ? valor_si_verdadero : valor_si_falso
//En este caso:
// - condicion: edad >=18
// - valor_si_verdadero: "Mayor de edad"
// - valor_si_faslo: "Menor de edad"